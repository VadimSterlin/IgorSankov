/* ==============================================
   STATE
   ============================================== */
let currentLang = 'en';

/* ==============================================
   LANGUAGE DETECTION & SWITCHING
   ============================================== */
function detectLanguage() {
    var saved = localStorage.getItem('lang');
    if (saved && translations[saved]) {
        return saved;
    }
    return 'en';
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;

    // Update all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        if (translations[lang][key] !== undefined) {
            el.textContent = translations[lang][key];
        }
    });

    // Update all data-i18n-alt elements (alt text for images)
    document.querySelectorAll('[data-i18n-alt]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-alt');
        if (translations[lang][key] !== undefined) {
            el.alt = translations[lang][key];
        }
    });

    // Update language switch active state
    document.querySelectorAll('.lang-switch__option').forEach(function(el) {
        el.classList.toggle('active', el.getAttribute('data-lang') === lang);
    });

    // Update share links
    updateShareLinks();
}

/* ==============================================
   STICKY HEADER
   ============================================== */
function initStickyHeader() {
    var header = document.getElementById('header');
    if (!header) return;

    window.addEventListener('scroll', function() {
        if (window.scrollY > 50) {
            header.classList.add('header--scrolled');
        } else {
            header.classList.remove('header--scrolled');
        }
    }, { passive: true });
}

/* ==============================================
   MOBILE MENU
   ============================================== */
function initMobileMenu() {
    var burger = document.getElementById('burgerBtn');
    var menu = document.getElementById('mobileMenu');
    if (!burger || !menu) return;

    burger.addEventListener('click', function() {
        var isOpen = menu.classList.toggle('active');
        burger.classList.toggle('active');
        menu.setAttribute('aria-hidden', !isOpen);
        burger.setAttribute('aria-label', isOpen ? 'Close menu' : 'Menu');
        document.body.classList.toggle('no-scroll', isOpen);
    });

    // Close menu on link click
    menu.querySelectorAll('.mobile-menu__link').forEach(function(link) {
        link.addEventListener('click', function() {
            menu.classList.remove('active');
            burger.classList.remove('active');
            menu.setAttribute('aria-hidden', 'true');
            burger.setAttribute('aria-label', 'Menu');
            document.body.classList.remove('no-scroll');
        });
    });
}

/* ==============================================
   SMOOTH SCROLL
   ============================================== */
function initSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(function(anchor) {
        anchor.addEventListener('click', function(e) {
            var targetId = this.getAttribute('href');
            if (targetId === '#') return;
            var target = document.querySelector(targetId);
            if (!target) return;
            e.preventDefault();

            var headerHeight = document.getElementById('header').offsetHeight;
            var targetPosition = target.getBoundingClientRect().top + window.pageYOffset - headerHeight;

            window.scrollTo({
                top: targetPosition,
                behavior: 'smooth'
            });
        });
    });
}

/* ==============================================
   COPY TO CLIPBOARD
   ============================================== */
function copyToClipboard(text, button) {
    var lang = currentLang;

    function onSuccess() {
        var textSpan = button.querySelector('[data-i18n]');
        if (textSpan) {
            textSpan.textContent = translations[lang].copy_done;
        }
        button.classList.add('copied');

        setTimeout(function() {
            if (textSpan) {
                textSpan.textContent = translations[lang].copy_btn;
            }
            button.classList.remove('copied');
        }, 2000);
    }

    function onFail() {
        // Fallback: select the text for manual copy
        var addr = button.closest('.crypto-card__address-row').querySelector('.crypto-card__address');
        if (addr) {
            var range = document.createRange();
            range.selectNodeContents(addr);
            var sel = window.getSelection();
            sel.removeAllRanges();
            sel.addRange(range);
        }
    }

    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(text).then(onSuccess).catch(function() {
            // Fallback for clipboard API failure
            fallbackCopy(text, onSuccess, onFail);
        });
    } else {
        fallbackCopy(text, onSuccess, onFail);
    }
}

function fallbackCopy(text, onSuccess, onFail) {
    var textarea = document.createElement('textarea');
    textarea.value = text;
    textarea.style.position = 'fixed';
    textarea.style.left = '-9999px';
    document.body.appendChild(textarea);
    textarea.focus();
    textarea.select();

    try {
        var success = document.execCommand('copy');
        if (success) {
            onSuccess();
        } else {
            onFail();
        }
    } catch (err) {
        onFail();
    }

    document.body.removeChild(textarea);
}

function initCopyButtons() {
    document.querySelectorAll('.crypto-card__copy').forEach(function(btn) {
        btn.addEventListener('click', function() {
            var address = this.getAttribute('data-copy');
            copyToClipboard(address, this);
        });
    });
}

/* ==============================================
   LIGHTBOX
   ============================================== */
function initLightbox() {
    var lightbox = document.getElementById('lightbox');
    var lightboxImg = document.getElementById('lightboxImg');
    var lightboxClose = document.getElementById('lightboxClose');
    var lightboxPrev = document.getElementById('lightboxPrev');
    var lightboxNext = document.getElementById('lightboxNext');
    if (!lightbox || !lightboxImg || !lightboxClose) return;

    // Collect all lightbox-able images into a single ordered list
    var lightboxItems = []; // { src, alt }
    var currentIndex = -1;

    document.querySelectorAll('.gallery__item img').forEach(function(img) {
        lightboxItems.push({ src: img.src, alt: img.alt });
    });
    document.querySelectorAll('[data-lightbox]').forEach(function(card) {
        var img = card.querySelector('img');
        lightboxItems.push({
            src: card.getAttribute('data-lightbox'),
            alt: img ? img.alt : ''
        });
    });

    function showImage(index) {
        if (index < 0 || index >= lightboxItems.length) return;
        currentIndex = index;
        lightboxImg.src = lightboxItems[index].src;
        lightboxImg.alt = lightboxItems[index].alt;
        // Update nav button visibility
        if (lightboxPrev) lightboxPrev.style.display = lightboxItems.length > 1 ? '' : 'none';
        if (lightboxNext) lightboxNext.style.display = lightboxItems.length > 1 ? '' : 'none';
    }

    function openLightbox(index) {
        showImage(index);
        lightbox.classList.add('active');
        lightbox.setAttribute('aria-hidden', 'false');
        document.body.classList.add('no-scroll');
    }

    function navigatePrev() {
        if (currentIndex > 0) {
            showImage(currentIndex - 1);
        } else {
            showImage(lightboxItems.length - 1); // wrap around
        }
    }

    function navigateNext() {
        if (currentIndex < lightboxItems.length - 1) {
            showImage(currentIndex + 1);
        } else {
            showImage(0); // wrap around
        }
    }

    // Open lightbox from gallery images
    document.querySelectorAll('.gallery__item img').forEach(function(img, i) {
        img.addEventListener('click', function() {
            openLightbox(i);
        });
    });

    // Open lightbox from document cards
    var galleryCount = document.querySelectorAll('.gallery__item img').length;
    document.querySelectorAll('[data-lightbox]').forEach(function(card, i) {
        card.addEventListener('click', function() {
            openLightbox(galleryCount + i);
        });
    });

    function closeLightbox() {
        lightbox.classList.remove('active');
        lightbox.setAttribute('aria-hidden', 'true');
        document.body.classList.remove('no-scroll');
        lightboxImg.src = '';
        currentIndex = -1;
    }

    lightboxClose.addEventListener('click', closeLightbox);

    // Prev/Next button click handlers
    if (lightboxPrev) {
        lightboxPrev.addEventListener('click', function(e) {
            e.stopPropagation();
            navigatePrev();
        });
    }
    if (lightboxNext) {
        lightboxNext.addEventListener('click', function(e) {
            e.stopPropagation();
            navigateNext();
        });
    }

    // Close on background click
    lightbox.addEventListener('click', function(e) {
        if (e.target === lightbox) {
            closeLightbox();
        }
    });

    // Keyboard navigation: Escape, ArrowLeft, ArrowRight
    document.addEventListener('keydown', function(e) {
        if (!lightbox.classList.contains('active')) return;
        if (e.key === 'Escape') {
            closeLightbox();
        } else if (e.key === 'ArrowLeft') {
            navigatePrev();
        } else if (e.key === 'ArrowRight') {
            navigateNext();
        }
    });
}

/* ==============================================
   SHARE BUTTONS
   ============================================== */
function updateShareLinks() {
    var pageUrl = window.location.href;
    var shareText = currentLang === 'ru'
        ? 'Помогите Игорю Санкову в борьбе с мышечной дистрофией. Каждый репост важен.'
        : 'Help Igor Sankov fight muscular dystrophy. Every share matters.';
    var twitterShareText = shareText + ' \ud83e\udd1d';

    var twitterBtn = document.getElementById('shareTwitter');
    if (twitterBtn) {
        twitterBtn.href = 'https://twitter.com/intent/tweet?text=' + encodeURIComponent(twitterShareText) + '&url=' + encodeURIComponent(pageUrl);
    }

    var telegramBtn = document.getElementById('shareTelegram');
    if (telegramBtn) {
        telegramBtn.href = 'https://t.me/share/url?url=' + encodeURIComponent(pageUrl) + '&text=' + encodeURIComponent(shareText);
    }

    var whatsappBtn = document.getElementById('shareWhatsApp');
    if (whatsappBtn) {
        whatsappBtn.href = 'https://wa.me/?text=' + encodeURIComponent(shareText + ' ' + pageUrl);
    }
}

function initShareButtons() {
    updateShareLinks();

    var copyLinkBtn = document.getElementById('shareCopyLink');
    if (copyLinkBtn) {
        copyLinkBtn.addEventListener('click', function() {
            var lang = currentLang;
            var btn = this;
            var textSpan = btn.querySelector('[data-i18n]');
            var pageUrl = window.location.href;

            if (navigator.clipboard && navigator.clipboard.writeText) {
                navigator.clipboard.writeText(pageUrl).then(function() {
                    if (textSpan) textSpan.textContent = translations[lang].share_link_copied;
                    btn.classList.add('copied');
                    setTimeout(function() {
                        if (textSpan) textSpan.textContent = translations[lang].share_copy_link;
                        btn.classList.remove('copied');
                    }, 2000);
                });
            } else {
                fallbackCopy(pageUrl, function() {
                    if (textSpan) textSpan.textContent = translations[lang].share_link_copied;
                    btn.classList.add('copied');
                    setTimeout(function() {
                        if (textSpan) textSpan.textContent = translations[lang].share_copy_link;
                        btn.classList.remove('copied');
                    }, 2000);
                }, function() {});
            }
        });
    }
}

/* ==============================================
   LANGUAGE SWITCH
   ============================================== */
function initLangSwitch() {
    document.querySelectorAll('.lang-switch__option').forEach(function(btn) {
        btn.addEventListener('click', function() {
            var lang = this.getAttribute('data-lang');
            if (lang && lang !== currentLang) {
                setLanguage(lang);
            }
        });
    });
}

/* ==============================================
   INIT
   ============================================== */
document.addEventListener('DOMContentLoaded', function() {
    // Detect and set language
    currentLang = detectLanguage();
    setLanguage(currentLang);

    // Fix OG meta tags with absolute URLs
    var origin = window.location.origin;
    var ogImage = document.querySelector('meta[property="og:image"]');
    if (ogImage && ogImage.content && !ogImage.content.startsWith('http')) {
        ogImage.content = origin + '/' + ogImage.content;
    }
    var ogUrl = document.querySelector('meta[property="og:url"]');
    if (ogUrl) {
        ogUrl.content = window.location.href;
    }
    var twitterImage = document.querySelector('meta[name="twitter:image"]');
    if (twitterImage && twitterImage.content && !twitterImage.content.startsWith('http')) {
        twitterImage.content = origin + '/' + twitterImage.content;
    }

    // Dynamic year in footer
    var yearEl = document.getElementById('currentYear');
    if (yearEl) {
        yearEl.textContent = new Date().getFullYear();
    }

    // Initialize all features
    initStickyHeader();
    initMobileMenu();
    initSmoothScroll();
    initCopyButtons();
    initLightbox();
    initShareButtons();
    initLangSwitch();
});
